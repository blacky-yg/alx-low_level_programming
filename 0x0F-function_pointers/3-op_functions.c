#include "3-calc.h"

/**
* op_add - Sum of 2 numbers
* @a: 1st num
* @b: 2nd num
*
* Description: Sum of 2 numbers
* Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Subs of 2 numbers
* @a: 1st num
* @b: 2nd num
*
* Description: Subs of 2 numbers
* Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Mul of 2 numbers
* @a: 1st num
* @b: 2nd num
*
* Description: Mul of 2 numbers
* Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - Div of 2 numbers
* @a: 1st num
* @b: 2nd num
*
* Description: Div of 2 numbers
* Return: a / b.
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - Modulo of 2 numbers
* @a: 1st num
* @b: 2nd num
*
* Description: Modulo of 2 numbers
* Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
